    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        if (txt_tenNhanVien.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "tên nhân viên không được để trống");
        } else {
            if (txt_soDienThoai.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "bạn chưa nhập số điện thoại");
            } else if (Integer.valueOf(txt_soDienThoai.getText()) <= -1) {
                JOptionPane.showMessageDialog(null, "số điện thoại không phải là số âm");
            } else if (Integer.valueOf(txt_soDienThoai.getText()).longValue() == 10) {
                JOptionPane.showMessageDialog(null, "số điện thoại không nhỏ hơn 10 số");
            } else if(!Pattern.matches("^[a-zA-Z]+$", txt_soDienThoai.getText())){
                
                if (txt_email.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "tên email không được để trống");
                } else if (!Pattern.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$", txt_email.getText())) {
                    JOptionPane.showMessageDialog(null, "email không đúng định dạng");
                } else {
                    if (txt_diaChi.getText().isEmpty()) {
                        JOptionPane.showMessageDialog(null, "địa chỉ không được để trống");
                    } else {
                        if (txt_idTaiKhoan.getText().isEmpty()) {
                            JOptionPane.showMessageDialog(null, "id tài khoản không được để trống");
                        } else {
                            String hoTen = txt_tenNhanVien.getText();
                            int gioiTinh;
                            if (rdo_nam.isSelected()) {
                                gioiTinh = 0;
                            } else {
                                gioiTinh = 1;
                            }
                            int soDienthoai = Integer.parseInt(txt_soDienThoai.getText());
                            String email = txt_email.getText();
                            String diaChi = txt_diaChi.getText();
                            int idTaiKhoan = Integer.parseInt(txt_idTaiKhoan.getText());
                            int trangThai;
                            if (rdo_dang.isSelected()) {
                                trangThai = 0;
                            } else {
                                trangThai = 1;
                            }
                            nhanVien nv = new nhanVien();
                            nv.setTenNhanVien(hoTen);
                            nv.setGioiTinh(gioiTinh);
                            nv.setSoDienThoai(soDienthoai);
                            nv.setDiaChi(diaChi);
                            nv.setEmail(email);
                            nv.setTaiKhoan(idTaiKhoan);
                            nv.setTrangThai(trangThai);
                            service.themNhanVien(nv);
                            showSoTrang();
                        }
                    }
                }
            }else {
                JOptionPane.showMessageDialog(null, "số điện thoại không phải là chữ");
            }
        }


    }//GEN-LAST:event_btn_themActionPerformed

