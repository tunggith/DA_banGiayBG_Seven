    public void loadData(long trang) {
        DefaultTableModel mol = new DefaultTableModel();
        mol = (DefaultTableModel) tbl_nhanVien.getModel();
        mol.setRowCount(0);
        mol.getDataVector().removeAllElements();
        try {
            String sql = "select top 3 id_nhanVien, hoTen, gioiTinh,soDt,email,diaChi,id_taiKhoan,trangThai from nhanVien where [id_nhanVien]not in (select top " + (trang * 3 - 3) + " [id_nhanVien] from nhanVien)";
            Statement stm = conn.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            while (rs.next()) {
                int idNhanVien = Integer.parseInt(rs.getString(1));
                String hoTen = rs.getString(2);
                int gioiTinh = Integer.parseInt(rs.getString(3));
                int soDienthoai = Integer.parseInt(rs.getString(4));
                String email = rs.getString(5);
                String diaChi = rs.getString(6);
                int idTaiKhoan = Integer.parseInt(rs.getString(7));
                int trangThai = Integer.parseInt(rs.getString(8));

                Vector vt = new Vector();
                vt.add(idNhanVien);
                vt.add(hoTen);
                String strGioiTinh;
                if (gioiTinh == 0) {
                    strGioiTinh = "nam";
                } else {
                    strGioiTinh = "nữ";
                }
                vt.add(strGioiTinh);
                vt.add(soDienthoai);
                vt.add(email);
                vt.add(diaChi);
                vt.add(idTaiKhoan);
                String strTrangThai;
                if (trangThai == 0) {
                    strTrangThai = "đang làm";
                } else {
                    strTrangThai = "đã nghỉ";
                }
                vt.add(strTrangThai);
                mol.addRow(vt);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        try {

        } catch (Exception e) {
        }
    }

