    public void loadData(long trang) {
        mol = (DefaultTableModel) tbl_khuyenMai.getModel();
        mol.setRowCount(0);
        Connection conn = connectionProvider.getConnection();
        mol.getDataVector().removeAllElements();
        try {
            String sql = "select top 3  id_khuyenMai,tenKhuyenMai,loai,giaTri,ngayBatDau,ngayKetThuc,trangThai from khuyenMai where [id_KhuyenMai]not in (select top " + (trang * 3 - 3) + " [id_khuyenMai] from khuyenMai)";
            Statement stm = conn.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            while (rs.next()) {
                Vector vt = new Vector();
                int ma = Integer.parseInt(rs.getString(1));
                String tenKm = rs.getString(2);
                String loai = rs.getString(3);
                String giaTri = rs.getString(4);
                String ngayBatDau = rs.getString(5);
                String ngayKetThuc = rs.getString(6);
                int trangThai = Integer.parseInt(rs.getString(7));

                vt.add(String.valueOf(ma));
                vt.add(tenKm);
                vt.add(loai);
                vt.add(giaTri);
                vt.add(ngayBatDau);
                vt.add(ngayKetThuc);
                String checkTT;
                if (trangThai == 1) {
                    checkTT = "đang áp dụng";
                } else {
                    checkTT = "chưa áp dụng";
                }
                vt.add(checkTT);

                mol.addRow(vt);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

