    void showIndex(int index) {
        String id_khuyenMai = tbl_khuyenMai.getValueAt(index, 0).toString();
        String tenKhuyenMai = tbl_khuyenMai.getValueAt(index, 1).toString();
        String loai = tbl_khuyenMai.getValueAt(index, 2).toString();
        String giaTri = tbl_khuyenMai.getValueAt(index, 3).toString();
        String trangThai = tbl_khuyenMai.getValueAt(index, 6).toString();

        try {
            Date ngaybatDau = new SimpleDateFormat("yyyy-MM-dd").parse((String) tbl_khuyenMai.getValueAt(index, 4));
            txt_ngayBatDau.setDate(ngaybatDau);
            Date ngayketThuc = new SimpleDateFormat("yyyy-MM-dd").parse((String) tbl_khuyenMai.getValueAt(index, 5));
            txt_ngayKetThuc.setDate(ngayketThuc);
        } catch (ParseException ex) {
            Logger.getLogger(view_khuyenMai.class.getName()).log(Level.SEVERE, null, ex);
        }
        txt_tenKm.setText(tenKhuyenMai);
        if (loai.equals("%")) {
            cbo_loai1.setSelectedItem("%");
        } else {
            cbo_loai1.setSelectedItem("VNĐ");
        }

        txt_giaTri.setText(giaTri);
        if (trangThai.equals("đang áp dụng")) {
            rdo_dang.setSelected(true);
        } else {
            rdo_chua.setSelected(true);
        }

    }

    void fillTable(String search) {

        mol = (DefaultTableModel) tbl_khuyenMai.getModel();
        mol.setRowCount(0);
        for (khuyenMai x : service.getAll(search)) {
            Object[] dataRow = new Object[]{x.getId_khuyenMai(), x.getTenKhuyenMai(), x.getLoai(), x.getGiaTri(), x.getNgayBatDau(), x.getNgayKetThuc(), x.checkTT()};
            mol.addRow(dataRow);
        }
    }

