    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        String tenKhuyenMai = txt_tenKm.getText();
        String loai = cbo_loai1.getSelectedItem().toString();
        String giaTri = txt_giaTri.getText();
        String ngayBatDau = sdf.format(txt_ngayBatDau.getDate());
        String ngayKetThuc = sdf.format(txt_ngayKetThuc.getDate());
        int trangThai;
        if (rdo_dang.isSelected()) {
            trangThai = 1;
        } else {
            trangThai = 0;
        }
        khuyenMai kh = new khuyenMai();
        kh.setTenKhuyenMai(tenKhuyenMai);
        kh.setLoai(loai);
        kh.setGiaTri(String.valueOf(giaTri));
        kh.setNgayBatDau(ngayBatDau);
        kh.setNgayKetThuc(ngayKetThuc);
        kh.setTrangThai(trangThai);

        if (txt_tenKm.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "vui lòng nhập tên khuyến mãi");
        } else {
            if (txt_giaTri.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "vui lòng nhập giá trị");
            } else {
                try {
                    if (Double.parseDouble(txt_giaTri.getText().trim()) <= -1) {
                        JOptionPane.showMessageDialog(null, "giá trị không phải là số âm");
                    } else {
                        if (txt_ngayBatDau.getDate() == null) {
                            JOptionPane.showMessageDialog(null, "vui lòng nhập ngày bắt đầu");
                        } else {
                            if (ngayKetThuc.trim().isEmpty()) {
                                JOptionPane.showMessageDialog(null, "vui lòng nhập ngày kết thúc");
                            } else {
                                service.themKhuyenMai(kh);
                                fillTable(search);
                            }

                        }
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "giá trị không phải là số");
                }
            }
        }

    }//GEN-LAST:event_btn_themActionPerformed

